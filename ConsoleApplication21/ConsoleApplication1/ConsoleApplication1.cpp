#include <iostream>
using namespace std;

// ref, reference, посилання у с++

// Посилання - це змінна, що являється псевдонімом для іншої
// змінної (копіює місце в пам'яті де зберігається значення
// і додає до цього значення ще одну назву)

// Задача 1. Створити функцію, що приймає значення змінної
// та ділить значення в цій змінній на 2.

int f1(int a) {
	return a / 2;
}

void f1(int* pa) {
	*pa /= 2;
}

void f2(int& ra) {
	ra /= 2;
}

int main()
{
	int x = 10;
	f2(x);
}
