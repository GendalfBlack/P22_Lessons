#include <iostream>
using namespace std; 

/* Рядок - масив символів що зберігає підряд певну кількість 
текстової інформації. */

int main()
{
	char* word = new char[50];
	
	//cin >> word; // поганий варіант
	fgets(word, 50, stdin); // гарний варіант
	/*перший аргумент - адреса(показчик) куди записувать
	  другий аргумент - об'єм пам'яті що доступна для запису
	  третій аргумент - stdin - з буферу консолі
	  */
	cout << word;
}
